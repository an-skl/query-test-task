package org.query.calc;

public class DoubleTuple {
    private final double v1;
    private final double v2;

    public DoubleTuple(double v1, double v2) {
        this.v1 = v1;
        this.v2 = v2;
    }

    public double getV1() {
        return v1;
    }

    public double getV2() {
        return v2;
    }
}
